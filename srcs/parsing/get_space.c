#include "cub3d.h"

int	g_s(char *str)
{
	int	i;

	i = 0;
	while (*str == ' ')
	{
		str++;
		i++;
	}
	i += 2;
	return (i);
}

int	g_s_color(char *str)
{
	int	i;

	i = 0;
	while (*str == ' ')
	{
		str++;
		i++;
	}
	i++;
	return (i);
}
